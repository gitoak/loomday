/// Contains predefined elevation values for shadows and depth.
/// These are DEFAULT values that can be customized if needed.
class Elevation {
  /// NONE: No elevation (0.0) - default for flat surfaces
  static const none = 0.0;
  /// SM: Small elevation (2.0) - default for subtle depth
  static const sm = 2.0;
  /// MD: Medium elevation (4.0) - default for cards, buttons
  static const md = 4.0;
  /// LG: Large elevation (8.0) - default for dialogs, menus
  static const lg = 8.0;
  /// XL: Extra Large elevation (16.0) - default for modals
  static const xl = 16.0;
}
